
# 📡 PROMPT BASE – Investigación Profunda GPT-4.5

## 🎯 Objetivo General

Desarrollar un **roadmap técnico realista y completo** para la implementación de ALMA_RESIST como sistema modular offline con IA local, enfocado en:

- Soberanía tecnológica y privacidad.
- Trabajo humano intensivo (10 hs diarias de dedicación).
- Modularidad y escalabilidad.
- Colaboración humano-IA (IA como copiloto real de desarrollo).

Este roadmap será ejecutado progresivamente, primero con GPT-4.5 y luego sostenido y expandido por GPT-4.0 y DeepSeek.


## 🧪 Qué debe generar GPT-4.5

### 🧭 1. Roadmap Técnico General

- Duración proyectada: **3 a 6 meses** (basado en ~10 hs diarias de trabajo humano)
- Entregables por fase (módulos funcionales)
- Estructura por semanas o sprints (etapas lógicas)
- Estimación de complejidad y orden de ejecución
- Requisitos técnicos y dependencias por módulo

### ✅ 2. Checklists por Módulo

- Listado detallado de pasos para cada componente (CLI, LLM, Loader, etc.)
- Precondiciones, tareas, entregables y pruebas

### 🛠️ 3. Propuesta de herramientas

- Lenguajes, librerías, frameworks mínimos necesarios
- Elección de motores LLM (llama.cpp, Mistral, DeepSeek)
- Recomendaciones de estructura de carpetas y paquetes

### 📊 4. Sugerencias adicionales

- Qué replantear de la estructura actual
- Qué preservar como base sólida
- Cómo garantizar mantenibilidad y seguridad


## 🧠 Contexto para la IA

